namespace System {
	internal class ColumnClickEventHandler {
		private object v;
		private object sender;
		private object columnClickEventArgs;
		private object e;

		public ColumnClickEventHandler(object v,object sender,object columnClickEventArgs,object e) {
			this.v=v;
			this.sender=sender;
			this.columnClickEventArgs=columnClickEventArgs;
			this.e=e;
		}
	}
}